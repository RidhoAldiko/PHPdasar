echo $a;
$a = 10; $a -= 7; //penugasan pengurangan
echo $a;
$a = 10; $a *= 7; //penugasan perkalian
echo $a;
$a = 10; $a /= 7; //penugasan pembagian 
echo $a;
$a = 10; $a %= 7; //penugasan sisa bagi 
echo $a;
