<?php
/* disimbolkan dengan <, >, <=, >=, ==, != */
/* operator perbandingan akan menghasilkan
output boolean(true or false) contoh : */
$a = 10; $b = 7;
var_dump($a >= $b);
/* operator perbandingan hanya membandingkan
nilai tanpa membandingkan tipe datanya contoh : */
$a = 10; $b = "10"; //variabel b adalah string
var_dump($a == $b);
?>


